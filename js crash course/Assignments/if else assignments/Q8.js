/*
  8. Write a program that takes the length of two legs of a right triangle 
  as input and calculates the area and hypotenuse?
  */

 let a;
 let b;

 a = prompt('enter a value one');
 b = prompt('enter a second value');

 if(a >= 50){
   alert( (a * a) + (b * b) );
 }
 else if(b <= 50){
   alert( (a * a) + (b * b));
 }


